import UIKit

extension UIColor {

    class var silverGrey: UIColor {
        return UIColor(white: 0.88, alpha: 255)
    }

    class var darkGreen: UIColor {
        return UIColor(red: 14.0 / 255.0, green: 228.0 / 255.0, blue: 166.0 / 255.0, alpha: 1.0)
    }
}
